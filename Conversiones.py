#Objetivo: invocar al módulo para conversión de un moneda a otra
#Entrada: cantidad en una moneda
#Procesos principales:
#Salida: Cantidad en la moneda convertida

#Conversor: 
def cm_a_metros(cms):
    metros = cms / 100.0
    return metros

def cm_a_kilometros(cms):
    kilometros = cms / 100000.0
    return kilometros

def cm_a_pulgadas(cms):
    pulgadas = cms / 2.54
    return pulgadas

def cm_a_pies(cms):
    pies = cms / 30.48
    return pies